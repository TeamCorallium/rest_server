<?php

namespace CoralliumServerBundle\Entity;

/**
 * Outcome
 */
class Outcome
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var int
     */
    private $quantityProducts;

    /**
     * @var float
     */
    private $income;

    /**
     * @var bool
     */
    private $newBusiness;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set quantityProducts
     *
     * @param integer $quantityProducts
     *
     * @return Outcome
     */
    public function setQuantityProducts($quantityProducts)
    {
        $this->quantityProducts = $quantityProducts;

        return $this;
    }

    /**
     * Get quantityProducts
     *
     * @return int
     */
    public function getQuantityProducts()
    {
        return $this->quantityProducts;
    }

    /**
     * Set income
     *
     * @param float $income
     *
     * @return Outcome
     */
    public function setIncome($income)
    {
        $this->income = $income;

        return $this;
    }

    /**
     * Get income
     *
     * @return float
     */
    public function getIncome()
    {
        return $this->income;
    }

    /**
     * Set newBusiness
     *
     * @param boolean $newBusiness
     *
     * @return Outcome
     */
    public function setNewBusiness($newBusiness)
    {
        $this->newBusiness = $newBusiness;

        return $this;
    }

    /**
     * Get newBusiness
     *
     * @return bool
     */
    public function getNewBusiness()
    {
        return $this->newBusiness;
    }
}
